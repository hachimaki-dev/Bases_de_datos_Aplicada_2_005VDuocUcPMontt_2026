/* ============================
   CREACIÓN DE TABLAS
   ============================ */

CREATE TABLE ARRIENDO_DRON
(id_arriendo        NUMBER(7)    NOT NULL CONSTRAINT PK_ARRIENDO_DRON PRIMARY KEY,
 placa              VARCHAR2(8)  NOT NULL,
 numrun_cli         NUMBER(10)   NOT NULL,
 fecha_ini_arriendo DATE         NOT NULL,
 dias_solicitados   NUMBER(6),
 fecha_devolucion   DATE);


CREATE TABLE TIPO_SALUD
(id_tipo_sal  VARCHAR2(3)   NOT NULL CONSTRAINT PK_TIPO_SALUD PRIMARY KEY,
 descripcion  VARCHAR2(100) NOT NULL,
 pct_sueldo   NUMBER(3,1)   NOT NULL);


CREATE TABLE CLIENTE
(numrun_cli     NUMBER(10)   NOT NULL CONSTRAINT PK_CLIENTE PRIMARY KEY,
 dvrun_cli      VARCHAR2(1)  NOT NULL,
 appaterno_cli  VARCHAR2(30) NOT NULL,
 apmaterno_cli  VARCHAR2(30) NOT NULL,
 pnombre_cli    VARCHAR2(30) NOT NULL,
 snombre_cli    VARCHAR2(30),
 direccion      VARCHAR2(60) NOT NULL,
 celular_cli    NUMBER(15),
 fono_fijo_cli  NUMBER(15),
 renta          NUMBER(7)    NOT NULL,
 fecha_nac_cli  DATE,
 id_region      NUMBER(3),
 id_tipo_cli    VARCHAR2(1)  NOT NULL,
 id_estado_civil NUMBER(2)   NOT NULL);


CREATE TABLE REGION
(id_region      NUMBER(3)    NOT NULL CONSTRAINT PK_REGION PRIMARY KEY,
 nombre_region  VARCHAR2(40) NOT NULL CONSTRAINT UN_REGION_NOMBRE UNIQUE);


CREATE TABLE EMPLEADO
(numrun_emp      NUMBER(10)   NOT NULL CONSTRAINT PK_EMPLEADO PRIMARY KEY,
 dvrun_emp       VARCHAR2(1)  NOT NULL,
 appaterno_emp   VARCHAR2(30) NOT NULL,
 apmaterno_emp   VARCHAR2(30) NOT NULL,
 pnombre_emp     VARCHAR2(25) NOT NULL,
 snombre_emp     VARCHAR2(25),
 direccion_emp   VARCHAR2(60) NOT NULL,
 sexo            CHAR(1)      NOT NULL CONSTRAINT CHK_SEXO CHECK (sexo IN ('F','M')),
 celular_emp     NUMBER(15),
 fono_fijo_emp   NUMBER(15),
 fecha_nac       DATE         NOT NULL,
 fecha_contrato  DATE         NOT NULL,
 sueldo_base     NUMBER(7)    NOT NULL,
 id_region       NUMBER(3),
 id_tipo_sal     VARCHAR2(3)  NOT NULL,
 id_estado_civil NUMBER(2)    NOT NULL);


CREATE TABLE MARCA
(id_marca      NUMBER(2)    NOT NULL CONSTRAINT PK_MARCA PRIMARY KEY,
 nombre_marca  VARCHAR2(25) NOT NULL);


CREATE TABLE TIPO_CLIENTE
(id_tipo_cli      VARCHAR2(1)  NOT NULL CONSTRAINT PK_TIPO_CLIENTE PRIMARY KEY,
 nombre_tipo_cli  VARCHAR2(20) NOT NULL);


CREATE TABLE TIPO_DRON
(id_tipo_dron      VARCHAR2(1)  NOT NULL CONSTRAINT PK_TIPO_DRON PRIMARY KEY,
 nombre_tipo_dron  VARCHAR2(40) NOT NULL);


CREATE TABLE DRON
(placa             VARCHAR2(8)  NOT NULL CONSTRAINT PK_DRON PRIMARY KEY,
 color             VARCHAR2(15) NOT NULL,
 modelo            VARCHAR2(30) NOT NULL,
 rango_km        NUMBER(4)    NOT NULL,
 anio_fab          NUMBER(4),
 valor_arriendo_dia NUMBER(7)   NOT NULL,
 valor_garantia_dia NUMBER(6),
 id_tipo_dron      VARCHAR2(1)  NOT NULL,
 numrun_emp        NUMBER(10),
 id_marca          NUMBER(2)    NOT NULL);


CREATE TABLE ESTADO_CIVIL
(id_estado_civil      NUMBER(2)    NOT NULL CONSTRAINT ESTADO_CIVIL_PK PRIMARY KEY,
 nombre_estado_civil  VARCHAR2(25) NOT NULL);


CREATE TABLE PORC_BONIF_ANNOS
(sueldo_desde  NUMBER(7,0) NOT NULL,
 sueldo_hasta  NUMBER(7,0) NOT NULL,
 porcentaje    NUMBER(5,2) NOT NULL);


CREATE TABLE HIST_ARRIENDO_ANUAL_DRON
(placa            VARCHAR2(8)  NOT NULL,
 modelo           VARCHAR2(30) NOT NULL,
 ano_proceso      NUMBER(4)    NOT NULL,
 cant_arriendos   NUMBER(6)    NOT NULL,
 total_dias       NUMBER(6)    NOT NULL);



